import React, { Component, ErrorInfo, ReactNode } from "react";

interface Props {
    children: ReactNode;
}

interface State {
    hasError: boolean;
    error: Error | null;
}

class ErrorBoundary extends Component<Props, State> {
    public state: State = {
        hasError: false,
        error: null,
    };

    public static getDerivedStateFromError(error: Error): State {
        return { hasError: true, error };
    }

    public componentDidCatch(error: Error, errorInfo: ErrorInfo) {
        console.error("Uncaught error:", error, errorInfo);
    }

    public render() {
        if (this.state.hasError) {
            return (
                <div className="p-10 bg-red-50 text-red-900 border border-red-200 m-10 rounded">
                    <h1 className="text-2xl font-bold mb-4">Une erreur est survenue (Frontend Crash)</h1>
                    <pre className="bg-red-100 p-4 rounded overflow-auto">
                        {this.state.error?.message}
                    </pre>
                    <button
                        onClick={() => window.location.href = '/dashboard'}
                        className="mt-4 bg-red-600 text-white px-4 py-2 rounded hover:bg-red-700"
                    >
                        Retour accueil
                    </button>
                </div>
            );
        }

        return this.props.children;
    }
}

export default ErrorBoundary;
